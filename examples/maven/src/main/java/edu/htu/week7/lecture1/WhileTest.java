package edu.htu.week7.lecture1;

public class WhileTest {
	public static void main(String[] args) {
		//Loop
		int counter = 0;
		//while(condition) : condition should have a result of true or false 
		while (counter < 10) {
			System.out.println("Hello");
			counter++;
		}
		System.out.println("Done");
	}
}
